// Площадь прямоугольника

#include <iostream>

using namespace std; 

int main()
{
    int a, b;
    cout << "Введите длину и ширину вашего прямоугольника" << endl;
    cout << "длина: ";
    cin >> a;
    cout << "ширина: ";
    cin >> b;
    cout << "Площадь вашего прямоугольника составляет " << a * b << endl;
}
